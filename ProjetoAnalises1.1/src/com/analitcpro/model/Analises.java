package com.analitcpro.model;

public class Analises {
	private int id;
	private String analiseTexto;
	private int analiseNota;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAnaliseTexto() {
		return analiseTexto;
	}

	public void setAnaliseTexto(String analiseTexto) {
		this.analiseTexto = analiseTexto;
	}

	public int getAnaliseNota() {
		return analiseNota;
	}

	public void setAnaliseNota(int analiseNota) {
		this.analiseNota = analiseNota;
	}
}
